package beans;

import java.time.LocalDate;

public class ST {
	private String name;
	private LocalDate birthDate;
	private int kor;
	private int eng;
	private int math;
	public ST() {
	}
	public ST(String name, LocalDate birthDate, int kor, int eng, int math) {
		this.name = name;
		this.birthDate = birthDate;
		this.kor = kor;
		this.eng = eng;
		this.math = math;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public LocalDate getBirthDate() {
		return birthDate;
	}
	public void setBirthDate(LocalDate birthDate) {
		this.birthDate = birthDate;
	}
	public int getKor() {
		return kor;
	}
	public void setKor(int kor) {
		this.kor = kor;
	}
	public int getEng() {
		return eng;
	}
	public void setEng(int eng) {
		this.eng = eng;
	}
	public int getMath() {
		return math;
	}
	public void setMath(int math) {
		this.math = math;
	}
}
